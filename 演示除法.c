/*divide.c——演示除法*/
#include<stdio.h>//并不能真正用浮點數除以整數，編譯器會先將兩個運算對象轉換成相同類型
int main(void)
{
	printf("整數除法：5/4係%d\n", 5 / 4);
	printf("整數除法：6/3係%d\n", 6 / 3);
	printf("整數除法：7/4係%d\n", 7 / 4);//整數除法會截斷計算結果的小數部分，并不會四捨五入
	printf("整數除法：5./4.係%1.2f\n", 7. / 4.);
	printf("整數除法：7./4係%1.2f\n", 7. / 4);
	printf("%d", 6 * 12 + 5 * 20);
	return 0;
}